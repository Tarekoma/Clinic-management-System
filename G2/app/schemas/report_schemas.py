class MedicalImageCreate(BaseModel):
    """
    Payload for POST /visits/{visit_id}/images  (doctor or assistant).

    image_url is set by the service layer after the file is saved to storage.
    Clients send the binary file via multipart/form-data — this schema
    carries the metadata that accompanies the upload.
    """

    model_config = ConfigDict(from_attributes=True)

    image_type: ImageType = Field(
        description="Modality of the image. Drives AI analysis pipeline selection.",
    )
    doctor_notes: str | None = Field(
        default=None,
        description="Initial clinical notes on this image. Can be updated later.",
    )


class MedicalImageUpdate(BaseModel):
    """
    Payload for PATCH /images/{id}  (doctor only).
    Used to update the doctor's notes after AI analysis is complete.
    image_url and image_type are immutable once set.
    """

    model_config = ConfigDict(from_attributes=True)

    doctor_notes: str | None = None


class MedicalImageResponse(BaseModel):
    """
    Full medical image record including AI diagnosis and doctor notes.
    image_url is the resolved download path.
    """

    model_config = ConfigDict(from_attributes=True)

    id:           int
    visit_id:     int
    image_url:    str
    image_type:   ImageType
    ai_diagnosis: str | None
    doctor_notes: str | None
    created_at:   datetime



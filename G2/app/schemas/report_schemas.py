"""
app/schemas/report_schemas.py

Request and response schemas for clinical outputs and compliance.

Covers:
- MedicalReport     (AI-assisted report lifecycle)
- MedicalImage      (uploaded images + AI analysis)
- LabReport         (uploaded lab documents + AI interpretation)
- AuditLog          (compliance trail — read-only from API perspective)

AI field design
---------------
ai_medications and ai_recommendations are JSONB in the database. The API
exposes them through typed Pydantic models (AIm edication, list[str]) so
the contract is explicit and clients don't have to parse raw JSON.

On the way IN (voice transcription, approval) the service layer populates
these from the AI module output. On the way OUT (responses) they are
serialized as structured objects.

Nesting strategy
----------------
MedicalReportResponse embeds VisitSummary (id + status + start_time).
It does NOT re-embed MedicalImage or LabReport lists — those live on the
visit and are fetched via their own endpoints to keep payloads lean.

AuditLog is read-only — no Create/Update schemas exist.
"""

from __future__ import annotations

from datetime import datetime
from typing import Any

from pydantic import BaseModel, ConfigDict, Field

from app.core.constants import AuditAction, ImageType, ReportStatus
from app.schemas.clinic_schemas import VisitSummary
from app.schemas.user_schemas import DoctorSummary


# ===========================================================================
# AI structured output sub-models
# ===========================================================================

class AIMedication(BaseModel):
    """
    A single medication entry as generated by the medical NLP module.
    Stored as one element inside the ai_medications JSONB array.
    """

    model_config = ConfigDict(from_attributes=True)

    name:      str = Field(description="Drug name (generic or brand).")
    dose:      str | None = Field(default=None, description="e.g. '500mg', '10ml'.")
    frequency: str | None = Field(default=None, description="e.g. 'twice daily', 'every 8 hours'.")
    duration:  str | None = Field(default=None, description="e.g. '7 days', '2 weeks'.")
    route:     str | None = Field(default=None, description="e.g. 'oral', 'topical', 'IV'.")
    notes:     str | None = Field(default=None, description="Special instructions.")


class AIReportDraft(BaseModel):
    """
    Structured AI output returned after voice transcription processing.
    Presented to the doctor for review before report approval.

    This schema is returned by the AI processing endpoint but is NOT
    stored directly — its fields map to MedicalReport columns.
    """

    model_config = ConfigDict(from_attributes=True)

    transcription:    str = Field(description="Verbatim speech-to-text output.")
    ai_diagnosis:     str | None = Field(
        default=None,
        description="SOAP-formatted diagnostic summary generated by medical NLP.",
    )
    ai_medications:     list[AIMedication] = Field(
        default_factory=list,
        description="Suggested medication list with structured dose/frequency.",
    )
    ai_recommendations: list[str] = Field(
        default_factory=list,
        description="Free-text lifestyle and follow-up recommendations.",
    )
    ai_follow_up: str | None = Field(
        default=None,
        description="Suggested follow-up interval e.g. 'Return in 2 weeks if symptoms persist'.",
    )


# ===========================================================================
# MEDICAL REPORT
# ===========================================================================

class MedicalReportCreate(BaseModel):
    """
    Payload for POST /visits/{visit_id}/report  (doctor only).

    Used when a report is created without voice — the doctor types findings
    directly. For voice-driven reports the service layer creates the report
    automatically after transcription.

    All AI fields are optional here because the service may populate them
    from AI output rather than from this payload.
    """

    model_config = ConfigDict(from_attributes=True)

    doctor_voice_transcription: str | None = Field(
        default=None,
        description="Raw transcription text if already obtained from speech-to-text.",
    )
    ai_diagnosis:               str | None = None
    ai_medications:             list[AIMedication] | None = None
    ai_recommendations:         list[str] | None = None
    ai_follow_up:               str | None = Field(default=None, max_length=500)
    doctor_notes:               str | None = Field(
        default=None,
        description="Doctor's manual additions or corrections to AI-generated content.",
    )


class MedicalReportUpdate(BaseModel):
    """
    Payload for PATCH /reports/{id}  (doctor only).

    Used by the doctor to edit the AI draft before approval.
    All fields optional — partial updates supported.
    """

    model_config = ConfigDict(from_attributes=True)

    doctor_voice_transcription: str | None = None
    ai_diagnosis:               str | None = None
    ai_medications:             list[AIMedication] | None = None
    ai_recommendations:         list[str] | None = None
    ai_follow_up:               str | None = Field(default=None, max_length=500)
    doctor_notes:               str | None = None


class MedicalReportStatusUpdate(BaseModel):
    """
    Dedicated payload for PATCH /reports/{id}/status  (doctor only).
    Separates lifecycle transitions (draft → approved → finalized)
    from field edits. The service layer validates allowed transitions.
    """

    model_config = ConfigDict(from_attributes=True)

    status: ReportStatus = Field(
        description=(
            "Target lifecycle status. "
            "Valid transitions: draft→reviewed, reviewed→approved, approved→finalized."
        ),
    )


class MedicalReportResponse(BaseModel):
    """
    Full medical report as returned to clients.

    ai_medications is exposed as a structured list of AIMedication objects
    rather than raw JSON, giving clients a typed contract.
    ai_recommendations is a list of plain strings.

    Embeds:
    - visit: VisitSummary (id, appointment_id, status, start_time)
    - doctor: DoctorSummary (id, name, specialization)
    """

    model_config = ConfigDict(from_attributes=True)

    id:       int
    visit_id: int
    doctor_id: int
    status:   ReportStatus

    doctor_voice_transcription: str | None
    ai_diagnosis:               str | None
    ai_medications:             list[AIMedication] | None
    ai_recommendations:         list[str] | None
    ai_follow_up:               str | None
    doctor_notes:               str | None

    # Shallow embeds
    visit:  VisitSummary
    doctor: DoctorSummary

    # Timestamps included on reports — clinicians need to know when generated
    created_at: datetime
    updated_at: datetime


class MedicalReportSummary(BaseModel):
    """
    Compact report for listing endpoints (e.g. GET /visits/{id}/history).
    """

    model_config = ConfigDict(from_attributes=True)

    id:         int
    visit_id:   int
    doctor_id:  int
    status:     ReportStatus
    created_at: datetime


